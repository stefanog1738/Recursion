func factorial(from n: Int) -> Int {
    print("Calculating factorial(\(n))...")
    if n <= 1 {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let hello = n * factorial(from: n-1)
        print("Non-terminal case, returning \(hello).")
        return hello
    }
}
print("The final result is: \(factorial(from: n)).")

